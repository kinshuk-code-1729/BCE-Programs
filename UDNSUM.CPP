#include <stdio.h>

int main()
{
  int a,b,sum;
  printf("Enter first number : ");
  scanf("%d",&a);
  printf("\nEnter second number : ");
  scanf("%d",&b);
  sum=a+b;
  printf("\nSum of %d and %d is : %d",a,b,sum);
  return 0;

}
